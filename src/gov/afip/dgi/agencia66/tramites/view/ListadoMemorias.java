    private void mostrarCasos() {
        
        List resultado = buscarCasos();
        
        DefaultTableModel modelo;
        
        
        String[] encabezado = {"id","Razon Social", "Solicitud", "Puesto Venta", "Fecha Proceso", "Agente"};
        
        modelo = new DefaultTableModel(null, encabezado);
        String[] registro;
        ControladorFiscal _cf;
        int size = resultado.size();
        
        System.out.println("Encontre:  " +size);
        txtInforma.setText("Se encontraron "+size +" casos de retiro de memoria fiscal");
                
         
        if(size< 1) {
            JOptionPane.showMessageDialog(this, "No se encontaron casos", "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
        }else {
            System.out.println("Procesando ...");
            for(int i=0; size > i; i++) {
                System.out.println("Procesando ..." +i);
                _cf = (ControladorFiscal) resultado.get(i);
                modelo.insertRow(i, new Object[] {_cf.getId(),_cf.getRazonSocial(),_cf.getSolicitud(), _cf.getPuestoVenta(), _cf.getFechaPoceso(), _cf.getAgente() });
                tblResultados.setModel(modelo);
            }
        }
    }

    private List buscarCasos() {
        BuscarControladorFiscal bcf;
        bcf = new BuscarControladorFiscal();
        return bcf.findCFSinMemoria();
    }
